    private void tipoRelatorioComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoRelatorioComboBoxActionPerformed
        int sel = this.tipoRelatorioComboBox.getSelectedIndex();
        
        switch (sel) {
            case 0:     // Reltorio de Atleta
                this.atletaPanel.setVisible(true);
                this.medicoPanel.setVisible(false);
                this.treinadorPanel.setVisible(false);
                break;
            case 1:     // Relatorio de Medico
                this.atletaPanel.setVisible(false);
                this.medicoPanel.setVisible(true);
                this.treinadorPanel.setVisible(false);
                break;
            case 2:     // Relatorio de Treinadores
                this.atletaPanel.setVisible(false);
                this.medicoPanel.setVisible(false);
                this.treinadorPanel.setVisible(true);
                this.geraRelatorioTreinadores();
                break;
            default:
                // May not happen
                break;
        }
        
    }//GEN-LAST:event_tipoRelatorioComboBoxActionPerformed

